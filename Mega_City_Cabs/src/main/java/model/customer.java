package model;

public class customer {
	
	int c_id;
	String c_name;
	String c_nic;
	int c_phone;
	String c_address;
	String c_email;
	String c_password;
	String c_cpassword;
	
	
	
	public int getC_id() {
		return c_id;
	}
	public void setC_id(int c_id) {
		this.c_id = c_id;
	}
	public String getC_name() {
		return c_name;
	}
	public void setC_name(String c_name) {
		this.c_name = c_name;
	}
	public String getC_nic() {
		return c_nic;
	}
	public void setC_nic(String c_nic) {
		this.c_nic = c_nic;
	}
	public int getC_phone() {
		return c_phone;
	}
	public void setC_phone(int c_phone) {
		this.c_phone = c_phone;
	}
	public String getC_address() {
		return c_address;
	}
	public void setC_address(String c_address) {
		this.c_address = c_address;
	}
	public String getC_email() {
		return c_email;
	}
	public void setC_email(String c_email) {
		this.c_email = c_email;
	}
	public String getC_password() {
		return c_password;
	}
	public void setC_password(String c_password) {
		this.c_password = c_password;
	}
	public String getC_cpassword() {
		return c_cpassword;
	}
	public void setC_cpassword(String c_cpassword) {
		this.c_cpassword = c_cpassword;
	}
}
