package model;

public class vehical {
	
	int v_id;
	String v_number;
	String v_type;
	String v_model;
	String v_seat;
	byte[] v_image;
	
	
	public int getV_id() {
		return v_id;
	}
	public void setV_id(int v_id) {
		this.v_id = v_id;
	}
	public String getV_number() {
		return v_number;
	}
	public void setV_number(String v_number) {
		this.v_number = v_number;
	}
	public String getV_type() {
		return v_type;
	}
	public void setV_type(String v_type) {
		this.v_type = v_type;
	}
	public String getV_model() {
		return v_model;
	}
	public void setV_model(String v_model) {
		this.v_model = v_model;
	}
	public String getV_seat() {
		return v_seat;
	}
	public void setV_seat(String v_seat) {
		this.v_seat = v_seat;
	}
	public byte[] getV_image() {
        return v_image;
    }

    public void setV_image(byte[] v_image) {
        this.v_image = v_image;
    }
	
	
}